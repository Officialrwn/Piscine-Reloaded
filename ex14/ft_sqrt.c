int	ft_sqrt(int nb)
{
	int	i;
	int	num;

	i = 1;
	num = 0;
	if (nb > 0)
	{
		while (i++ <= 46340)
		{
			if (i * i == nb)
				num = i;
		}
	}
	return (num);
}
